'''This is the main testing script for testing.

Optionally you can check the coverage of the tests using `coverage.py`. This will
tell you how much of your code your tests cover.
    To install:
        `pip install coverage`

    To run:
        `coverage run main_test.py` within the current directory

    To get the report:
        `coverage report -m`
'''

import unittest
import os


packages = [f.replace('.py','') for f in os.listdir('.') \
    if os.path.isfile(f) and '.' != f[0] and f != 'main.py' \
    and '.txt' not in f]

print('packages',packages)

for module in packages:
    print('\n\n')
    print('################################################')
    print('Testing Module {}'.format(module))
    print('################################################')
    unittest.main(
        module=module,
        verbosity=10,
        exit=False)

print('Finished Testing')
